export const evenOrOdd = (number) => {
  if (number % 2 === 0) {
    return 'even'
  } else {
    return 'odd'
  }
}

export const countTriangle = (base, height) => {
  return (base * height) / 2
}

export const multiply = (num1, num2) => {
  return num1 * num2
}

